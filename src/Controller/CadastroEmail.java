public class CadastroEmail {
    private String nome, email, senha, emailSaida;
    private int codigo, aceitaSsl, aceitaTls, emailDefinePadrao, porta;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getEmailSaida() {
        return emailSaida;
    }

    public void setEmailSaida(String emailSaida) {
        this.emailSaida = emailSaida;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public int getAceitaSsl() {
        return aceitaSsl;
    }

    public void setAceitaSsl(int aceitaSsl) {
        this.aceitaSsl = aceitaSsl;
    }

    public int getAceitaTls() {
        return aceitaTls;
    }

    public void setAceitaTls(int aceitaTls) {
        this.aceitaTls = aceitaTls;
    }

    public int getEmailDefinePadrao() {
        return emailDefinePadrao;
    }

    public void setEmailDefinePadrao(int emailDefinePadrao) {
        this.emailDefinePadrao = emailDefinePadrao;
    }

    public int getPorta() {
        return porta;
    }

    public void setPorta(int porta) {
        this.porta = porta;
    }

    
  
            
}
