    private void txtSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            Login();
        }
        if(Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_CAPS_LOCK)){
            labeLSenha.setText("CAPS LOOK ATIVADO");
        }else{
            labeLSenha.setText("");
        }
    }//GEN-LAST:event_txtSenhaKeyPressed

    private void txtLoginKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLoginKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            txtSenha.requestFocus();
        }
        if(Toolkit.getDefaultToolkit().getLockingKeyState(KeyEvent.VK_CAPS_LOCK)){
            labelLogin.setText("CAPS LOOK ATIVADO");
        }else{
            labelLogin.setText("");
        }
	
    }//GEN-LAST:event_txtLoginKeyPressed

    private void Login(){
        VerificaLogin log = new VerificaLogin();
        
        log.setLogin(txtLogin.getText());
        log.setSenha(txtSenha.getText());
        
        LoginDAO logDao = new LoginDAO();
        logDao.Login(log);
        
        if(log.isValidador()){
            
            String dataHora = null;
            dataHora = Data(dataHora);
            
            JOptionPane.showMessageDialog(this, "Login efetuado com sucesso! \n"
                    + "Usuario: "+log.getNome()+"\n"
                            + "Data: "+dataHora);

            new FormMenu(log).setVisible(true);
            dispose();
            
        }else if(log.getValidaLogin()=="LOGIN"){
            
            JOptionPane.showMessageDialog(this, "Senha invÃ¡lida, por favor, digite novamente!");
            txtSenha.requestFocus();
            txtSenha.selectAll();
            txtSenha.setSelectedTextColor(Color.yellow);
            
        }else if(log.getValidaSenha()=="SENHA"){
            
            JOptionPane.showMessageDialog(this, "Login invÃ¡lido, por favor, digite novamente!");
            txtLogin.requestFocus();
            txtLogin.selectAll();
            txtLogin.setSelectedTextColor(Color.yellow);
            
        }else{
            
            JOptionPane.showMessageDialog(this, "Login ou Senha invÃ¡lido, por favor, digite novamente!");
            txtLogin.requestFocus();
            txtLogin.selectAll();
            txtLogin.setSelectedTextColor(Color.yellow);
            txtSenha.setSelectedTextColor(Color.yellow);
            
        }
    }
    
    private String Data(String dataHora){
        Date data = new Date(System.currentTimeMillis());
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy - HH:mm:ss");
        String dataFormatada = dateFormat.format(data);
        dataHora = (dataFormatada);
        
        return dataHora;
    }
    
    
