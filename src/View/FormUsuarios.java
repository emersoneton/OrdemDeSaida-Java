    private void Alterar() {
        CadastroDeUsuario usu = new CadastroDeUsuario();

        usu.setNome(txtNome.getText());
        usu.setLogin(txtLogin2.getText());
        usu.setTipo((String) cBoxTipoDeUsuario.getSelectedItem());

        if (txtSenha.getText().trim().equals(txtRepitaSenha.getText())) {
            usu.setSenha(txtSenha.getText());

            UsuariosDAO usuDao = new UsuariosDAO();
            usuDao.Alterar(usu);

            Limpar();
        }


    }

