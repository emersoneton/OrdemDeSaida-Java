    private void BuscarUsuarios(){
        UsuariosDAO usuDao = new UsuariosDAO();
        List<CadastroDeUsuario> lista = usuDao.BuscarUsuarios();
        
        for(int x=0; x < lista.size(); x++){
            CadastroDeUsuario usu = new CadastroDeUsuario();
            
            usu.setNome(lista.get(x).getNome());
            usu.setLogin(lista.get(x).getLogin());
            usu.setTipo(lista.get(x).getTipo());
            
            tabelaUsuarios.addRow(usu);
        }
    }
    
