    private void Iniciar() {
        if (cBoxTipoDeUsuario.getSelectedItem() == "ADMINISTRADOR") {
            txtTipoDeUsuario.setText("O Administrador tem acesso a todo o sistema \n"
                    + "e controla todas as interações");
        }
    }

    private void Limpar() {
        txtLogin2.setText("");
        txtNome.setText("");
        txtRepitaSenha.setText("");
        txtSenha.setText("");
        
        int cont = Tabela.getRowCount();
        
        for(int x=0; x < cont; x++){
            tabelaUsuarios.removeRow(0);
        }
    }

