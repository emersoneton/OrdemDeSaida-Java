    private void Salvar() {

        CadastroDeUsuario usu = new CadastroDeUsuario();

        usu.setNome(txtNome.getText());
        usu.setLogin(txtLogin2.getText());
        usu.setTipo((String) cBoxTipoDeUsuario.getSelectedItem());

        if (txtSenha.getText().trim().equals(txtRepitaSenha.getText())) {
            usu.setSenha(txtSenha.getText());

            UsuariosDAO usuDao = new UsuariosDAO();
            usuDao.Salvar(usu);

        } else {
            JOptionPane.showMessageDialog(this, "Usuario e senha não conferem!");
            txtSenha.selectAll();
            txtSenha.setSelectedTextColor(Color.yellow);
            txtSenha.requestFocus();
            txtRepitaSenha.selectAll();
            txtRepitaSenha.setSelectedTextColor(Color.yellow);
        }

        if (usu.isValida()) {
            Limpar();
        }

    }

